console.log("Question 2 of the practice set chp2");

let fruit = "Apple";

switch (fruit) {
    case "Apple":
        console.log("You selected the Apple");
        break;
    case "Banana":
        console.log("You selected the Banana");
        break;
    case "Orange":
        console.log("You selected the Orange");
        break;
    default:
        console.log("Unknown Fruit");

}